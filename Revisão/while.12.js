// A) Criando a variável contagem e atribuindo um valor inicial
let contagem = 10

// B) Usando um loop while para criar uma contagem regressiva
while (contagem >= 0) {
    // C) Exibindo o valor atual da contagem no console
    console.log(contagem);
    // D) Diminuindo a contagem em 1
    contagem--
}

// E) Exibindo uma mensagem após o término da contagem regressiva
console.log("Contagem regressiva concluída!");
