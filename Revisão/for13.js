
// A) Escolhendo o número para a tabuada
let numero = 2;

// B) Usando um loop for para calcular e exibir os resultados da multiplicação
for (let i = 1; i <= 10; i++) {
    // C) Calculando a multiplicação e exibindo o resultado
    let resultado = numero * i;
    console.log(`${numero} x ${i} = ${resultado}`);
}
