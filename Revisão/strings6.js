// A) Criando as variáveis
let nome, idade, cidade;

// B) Atribuindo valores às variáveis
nome = "João";
idade = 30;
cidade = "São Paulo";

// C) Criando a mensagem usando template string
let mensagem = `Olá, meu nome é ${nome}, tenho ${idade} anos e moro em ${cidade}.`;

// D) Exibindo a mensagem no console
console.log(mensagem);

// E) Refazendo o item C utilizando concatenação
let mensagemConcatenada = "Olá, meu nome é " + nome + ", tenho " + idade + " anos e moro em " + cidade + ".";

// F) Exibindo a mensagem concatenada no console
console.log(mensagemConcatenada);
