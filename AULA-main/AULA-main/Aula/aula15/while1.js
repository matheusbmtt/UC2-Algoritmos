//While Significa ENQUANTO
let voltas = 0 // COMEÇO

while(voltas<10){ // CONDIÇÃO DE CONTINUAÇÃO
    console.log("Pedala") // AÇÃO REPETIDA
    voltas++ // INCREMENTO
}

